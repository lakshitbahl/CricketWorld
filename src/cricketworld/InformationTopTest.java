
package cricketworld;

public class InformationTopTest {
    private String name;
    private String matches;
    private String runs;
    private String wickets;
    private String w5;
    private String century;
    private String fifty;
    private String bestScore;
    private String w10;
    private String bestBowling;
    private String runsGiven;

    public InformationTopTest(String name, String matches, String runs, String wickets, String w5, String century, String fifty, String bestScore, String w10, String bestBowling, String runsGiven) {
        this.name = name;
        this.matches = matches;
        this.runs = runs;
        this.wickets = wickets;
        this.w5 = w5;
        this.century = century;
        this.fifty = fifty;
        this.bestScore = bestScore;
        this.w10 = w10;
        this.bestBowling = bestBowling;
        this.runsGiven = runsGiven;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getMatches() {
        return matches;
    }

    public void setMatches(String matches) {
        this.matches = matches;
    }

    public String getRuns() {
        return runs;
    }

    public void setRuns(String runs) {
        this.runs = runs;
    }

    public String getWickets() {
        return wickets;
    }

    public void setWickets(String wickets) {
        this.wickets = wickets;
    }

    public String getW5() {
        return w5;
    }

    public void setW5(String w5) {
        this.w5 = w5;
    }

    public String getCentury() {
        return century;
    }

    public void setCentury(String century) {
        this.century = century;
    }

    public String getFifty() {
        return fifty;
    }

    public void setFifty(String fifty) {
        this.fifty = fifty;
    }

    public String getBestScore() {
        return bestScore;
    }

    public void setBestScore(String bestScore) {
        this.bestScore = bestScore;
    }

    public String getW10() {
        return w10;
    }

    public void setW10(String w10) {
        this.w10 = w10;
    }

    public String getBestBowling() {
        return bestBowling;
    }

    public void setBestBowling(String bestBowling) {
        this.bestBowling = bestBowling;
    }

    public String getRunsGiven() {
        return runsGiven;
    }

    public void setRunsGiven(String runsGiven) {
        this.runsGiven = runsGiven;
    }

    
    
}
